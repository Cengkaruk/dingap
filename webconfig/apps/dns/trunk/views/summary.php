///////////////////////////////////////////////////////////////////////////////
// Load dependencies
///////////////////////////////////////////////////////////////////////////////

$this->lang->load('dns');
$this->lang->load('network');

///////////////////////////////////////////////////////////////////////////////
// Form handler
///////////////////////////////////////////////////////////////////////////////
 
///////////////////////////////////////////////////////////////////////////////
// Summary items
///////////////////////////////////////////////////////////////////////////////

foreach ($hosts as $real_ip => $entry) {

	$ip = $entry['ip'];
	$hostname = $entry['hostname'];
	$alias = (count($entry['aliases']) > 0) ? $entry['aliases'][0] : '';
	
	// Add '...' to indicate more aliases exist
	if (count($entry['aliases']) > 1)
		$alias .= " ..."; 

	///////////////////////////////////////////////////////////////////////////
	// Buttons
	///////////////////////////////////////////////////////////////////////////

	// Hide 127.0.0.1 entry

	if ($ip === '127.0.0.1') {
		continue;
	} else {
		$detail_buttons = button_set(
			array(
				anchor_edit('/app/dns/edit/' . $ip),
				anchor_delete('/app/dns/delete/' . $ip)
			)
		);
	}

	///////////////////////////////////////////////////////////////////////////
	// Summary Details
	///////////////////////////////////////////////////////////////////////////

	$details['title'] = $ip . " - " . $hostname;
	$details['edit_anchor'] = '/app/dns/edit/' . $ip;
	$details['details'] = array(
		$ip,
		$hostname,
		$alias,
		$detail_buttons
	);

	$items[] = $details;
}

///////////////////////////////////////////////////////////////////////////////
// Headers
///////////////////////////////////////////////////////////////////////////////

$headers = array(
	lang('network_ip'),
	lang('network_hostname'),
	lang('dns_aliases'),
	''
);

///////////////////////////////////////////////////////////////////////////////
// Summary table
///////////////////////////////////////////////////////////////////////////////

echo summary_table(
	lang('dns_local_dns_server'),
	$headers,
	$items
);

